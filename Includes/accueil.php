<h1>Bienvenue sur le site de gestion des étudiants de notre école.</h1>
<p>Ce site vous permet de vous inscrire et de consulter la liste des etudiants par filière.</p>
<p>Si vous etes un nouveau étudiant ou une nouvelle étudiante veuillez vous rendre dans l'onglet Inscription afin de vous inscrire.</p>
<p>Si vous etes deja inscrit alors rendez vous dans l'onglet Connexion afin d'accéder aux informations disponibles.</p>
<p>Pour avoir une idée des prestations que nous fournissons faite un tour dans la rubrique Présentation. Vous ne serez pas déçu.</p>
<p>Ou faite un tour sur nos page facebook Twitter Linkedin ou envoyer nous un mail en cliquant sur les icons qui s'afficheent en bas à droite</p>
<p>Nous vous proposons aussi deux langues.</p>
<p>Vous souhaitez nous contacter ou avoir plus d'informations cliquez sur Contact.</p>
<p>Bonne visite!</p>